import streamlit as st

def max_value(first, second, third):
  return max(first, second, third)

#Title
st.title("Max of three numbers")

#Input values
first = st.number_input("First number", value = 0)
second = st.number_input("Second number", value = 0)
third = st.number_input("Third number", value = 0)

#Button
if st.radio("Max number"):
  Max = max_value(first, second, third)
  st.success(f"Max val is {Max}")